    private void jButton1LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1LoginActionPerformed

        if (main.PASApplication.getQueryManager().checkMedewerker(this.txtUsername.getText(), new String(this.txtPassword.getPassword()))) {
            this.hasAccess = true;
            employeecode = Integer.parseInt(txtUsername.getText());

        } else {
            JOptionPane.showMessageDialog(null, "Ongeldige medewerkerscode en/of wachtwoord.");
        }
    }//GEN-LAST:event_jButton1LoginActionPerformed

