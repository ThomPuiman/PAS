    private void txtPasswordKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPasswordKeyPressed
        // TODO add your handling code here:
        int key = evt.getKeyCode();

    if (key == KeyEvent.VK_ENTER) {
            if (main.PASApplication.getQueryManager().checkMedewerker(this.txtUsername.getText(), new String(this.txtPassword.getPassword()))) {
                this.hasAccess = true;
                this.employeecode = Integer.parseInt(txtUsername.getText());

            } else {
                JOptionPane.showMessageDialog(null, "Ongeldige gebruikersnaam en/of wachtwoord.");
            }
        }
    }//GEN-LAST:event_txtPasswordKeyPressed

